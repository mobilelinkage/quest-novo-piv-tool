    function fillTotal(year, ageGroup, i) {
        const male = HFpEFTable[year].people_with_HFpEF_male[ageGroup][i];
        const female = HFpEFTable[year].people_with_HFpEF_female[ageGroup][i];

        return male + female;
    }

    function fillSumsTable(year, ageGroup) {
        sum = HFpEFTable[year].people_with_HFpEF_total[ageGroup].reduce((a, b) => a + b);
        sumsTable[year][ageGroup] = sum;
        sumsTable[year].total += sum;
    }

    function fillPercentage(year, ageGroup, i) {
        // On first run through function, fill in sums to be used throughout the bmi loop
        if (i === 0) {
            fillSumsTable(year, ageGroup);
        }

        return HFpEFTable[year].people_with_HFpEF_total[ageGroup][i] / sumsTable[year][ageGroup];
    }

    function fillHospitalization(year, ageGroup, i) {
        const roundedHalf = Math.floor(selectedInterventionData[year].BMI[i] * 2) / 2;
        return hospitalization_risk[`bmi${roundedHalf}`].risk;
    }

    function fillMortality(year, ageGroup, i) {
        const roundedHalf = Math.floor(selectedInterventionData[year].BMI[i] * 2) / 2;
        return OA_mortality.OA_mortality[`bmi${roundedHalf}`][ageGroup];
    }

    function fillWeightedAverage(year, ageGroup) {
        let weightedRisk = 0;
        let weightedMort = 0;
        const employees = sumsTable[year][ageGroup] / sumsTable[year].total;

        for (let i = 0; i < HFpEFTable[year].BMI_half_unit.length; i++) {
            const percentage = HFpEFTable[year].people_as_percentage_in_age_group[ageGroup][i];
            const hospitalization = HFpEFTable[year].hospitalization_risk[ageGroup][i];
            const mortality = HFpEFTable[year].mortality[ageGroup][i];

            weightedRisk += (percentage * hospitalization);
            weightedMort += (percentage * mortality);
        }

        HFpEFTable[year].weighted_average.weighted_risk[ageGroup] = weightedRisk;
        HFpEFTable[year].weighted_average.employees[ageGroup] = employees;
        HFpEFTable[year].weighted_average.weighted_mort[ageGroup] = weightedMort;

        HFpEFTable[year].weighted_average.overall_risk += weightedRisk * employees;
        HFpEFTable[year].weighted_average.overall_mortality += weightedMort * employees;
    }


    initiateSumsTable();
