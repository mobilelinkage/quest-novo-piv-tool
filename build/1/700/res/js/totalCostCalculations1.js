function buildTable1(apply_hcc = true, discount_rate = 0.03) {

    const interventions = [
        'AOM_1',
        'ILT',
        'NI',
        'AOM_Original'
    ];

    const headings = [
        { name: 'direct_medical_costs', function: fillCostsWithInflation },
        { name: 'direct_pharmacy_costs', function: fillCostsWithInflation },
        { name: 'absenteeism', function: fillCosts },
        { name: 'presenteeism', function: fillCosts },
        { name: 'workers_compensation_costs', function: fillCostsWithInflation },
        { name: 'disability_claim_costs', function: fillCostsWithInflation },
        { name: 'intervention_costs', function: fillInterventionCosts },
        { name: 'total_costs', function: fillTotalCosts },
        { name: 'cumulative_total_costs', function: fillCumulativeTotalCosts },
        { name: 'cumulative_investment', function: fillCumulativeInvestment }
    ];

    const baselines = [
        { name: 'direct_medical_costs', function: () => 'y' },
        { name: 'direct_pharmacy_costs', function: () => 'y' },
        { name: 'absenteeism', function: () => 'y' },
        { name: 'presenteeism', function: () => 'y' },
        { name: 'workers_compensation_costs', function: () => 'y' },
        { name: 'disability_claim_costs', function: () => 'y' },
        { name: 'intervention_costs', function: () => 'y' },
        { name: 'total_costs', function: () => 'y' },
        { name: 'cumulative_total_costs', function: () => 'y' },
        { name: 'cumulative_investment', function: () => 'y' }
    ];

    const inflation_factor = 1.05175121;

    const table1 = {};

    function initiateTable() {
        // Build a subtable for each intervention type
        interventions.forEach(intervention => {
            table1[intervention] = {};
            // Add each heading to table
            headings.forEach((heading, headingIndex) => {
                table1[intervention][heading.name] = {};
                // Add baseline value
                table1[intervention][heading.name].baseline = baselines[headingIndex].function();
                // Build a subheading for years 1 - 10
                for (let i = 1; i <= 10; i++) {
                    // Calculate value of table "cell" based on headers and year parameters
                    table1[intervention][heading.name][`Year${i}`] = heading.function(intervention, i, heading.name);
                }
            });
        });
    }

    // Functions to populate table "cells"

    function fillCosts(intervention, year, heading, inflation = 1) {
        const value = apply_hcc
            ? table2[intervention][heading][`Year${year}`] * inflation
            : ((table2[intervention][heading][`Year${year}`] + table2[intervention][heading].baseline) * inflation) / 2

        return value / (1 + discount_rate) ** year;
    }

    function fillCostsWithInflation(intervention, year, heading) {
        return fillCosts(intervention, year, heading, inflation_factor)
    }

    function fillInterventionCosts(intervention, year) {
        if (intervention === 'AOM_1') { 
            return table2.AOM_1.intervention_costs[`Year${year}`];
        };
        if (intervention === 'ILT') {
            const cost = year < 2 ? ILTCosts.Year1 : ILTCosts.Year2;
            return (cost * popByYear[`Year${year}`] * ILTCompleters[`Year${year}`] + (cost / 2) * popByYear[`Year${year}`] * ILTDiscontinuations[`Year${year}`]) / (1 + discount_rate) ** year;
        };
        if (intervention === 'NI') {
            return 0;
         };
        if (intervention === 'AOM_Original') {
            return table2.AOM_Original.intervention_costs[`Year${year}`];
         };
    }

    function fillTotalCosts(intervention, year) {
        let totalCosts = 0;
        for (let i = 0; i < 6; i++) {
            totalCosts += table1[intervention][headings[i].name][`Year${year}`];
        }
        return totalCosts;
    }

    function fillCumulativeTotalCosts(intervention, year) {
        return table1[intervention].total_costs[`Year${year}`] + (table1[intervention].cumulative_total_costs[`Year${year - 1}`] || 0);
    }

    function fillCumulativeInvestment(intervention, year) {
        return table1[intervention].intervention_costs[`Year${year}`] + (table1[intervention].cumulative_investment[`Year${year - 1}`] || 0);
    }



    initiateTable();
    return table1;
}





